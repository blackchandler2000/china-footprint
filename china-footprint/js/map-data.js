class MapDataManager {
    constructor() {
        this.provinceGeoData = null;
        this.loadedMaps = {};
    }

    // 获取省份列表
    getProvinces() {
        return PROVINCES;
    }

    // 获取省份简称
    getShortName(fullName) {
        return PROVINCE_SHORT_NAMES[fullName] || fullName;
    }

    // 获取省份对应的城市列表
    getCities(provinceName) {
        return CITIES_BY_PROVINCE[provinceName] || [];
    }

    // 获取城市对应的区县列表
    getCounties(provinceName, cityName) {
        const key = `${provinceName}-${cityName}`;
        return COUNTIES_BY_CITY[key] || this.getDefaultCounties(cityName);
    }

    // 默认区县（当没有具体数据时返回常见区县）
    getDefaultCounties(cityName) {
        // 常见区县后缀
        const suffixes = ['区', '县', '市'];
        if (cityName.endsWith('市')) {
            const base = cityName.slice(0, -1);
            return [
                `${base}区`,
                `${base}县`,
                `${base}新区`
            ];
        }
        return [cityName];
    }

    // 获取省份 GeoJSON
    async loadProvinceGeoData() {
        if (this.provinceGeoData) {
            return this.provinceGeoData;
        }

        try {
            const response = await fetch(PROVINCE_GEO_URL);
            if (!response.ok) {
                throw new Error(`HTTP error! status: ${response.status}`);
            }
            this.provinceGeoData = await response.json();
            return this.provinceGeoData;
        } catch (error) {
            console.error('加载省份地图数据失败:', error);
            throw error;
        }
    }

    // 注册省份地图到 ECharts
    async registerChinaMap(echarts) {
        try {
            const geoData = await this.loadProvinceGeoData();
            echarts.registerMap('china', geoData);
            return true;
        } catch (error) {
            console.error('注册中国地图失败:', error);
            return false;
        }
    }

    // 获取城市 GeoJSON URL
    getCityGeoUrl(provinceShortName) {
        // 使用公共城市 GeoJSON 数据源
        return `https://raw.githubusercontent.com/ymsong/geojson-china/master/geojson/city/${provinceShortName}.json`;
    }

    // 加载城市 GeoJSON
    async loadCityGeoData(provinceShortName) {
        if (this.loadedMaps[provinceShortName]) {
            return this.loadedMaps[provinceShortName];
        }

        try {
            const url = this.getCityGeoUrl(provinceShortName);
            const response = await fetch(url);
            if (!response.ok) {
                throw new Error(`HTTP error! status: ${response.status}`);
            }
            const geoData = await response.json();
            this.loadedMaps[provinceShortName] = geoData;
            return geoData;
        } catch (error) {
            console.error(`加载城市地图数据失败 (${provinceShortName}):`, error);
            return null;
        }
    }
}

